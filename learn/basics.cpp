// comment
#include <iostream>

using namespace std;


// main function
int main()
{
    // console out (cout), end line (endl)
    cout << "Hello world!" << endl;
    cout << "Patrick" << endl;

    // variables
    string person = "John";
    int age;
    age = 35;

    // using variables
    cout << "There once was a man named " << person << endl;
    cout << person << " is " << age << " years old" << endl;

    person = "Mike";
    age = 20;
    cout << "There once was a man named " << person << endl;
    cout << person << " is " << age << " years old" << endl;

    return 0;
}
